package DataStructures.Stacks.Palindrome;

public class Palindrome {
    public static void main(String[] args) {
        System.out.println(CheckPalindrome.checkPalindrome("abccba"));
        System.out.println(CheckPalindrome.checkPalindrome("Was it a car or a cat I saw?"));
        System.out.println(CheckPalindrome.checkPalindrome("I did, did I?"));
        System.out.println(CheckPalindrome.checkPalindrome("hello"));
        System.out.println(CheckPalindrome.checkPalindrome("Don't nod"));
    }
}
